package com.example.demo;


public class BoitAddress {
    private int id;
    private String addresseMail;
    private String passwordMail;
    private int codeBoite;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAddresseMail() {
        return addresseMail;
    }

    public void setAddresseMail(String addresseMail) {
        this.addresseMail = addresseMail;
    }

    public String getPasswordMail() {
        return passwordMail;
    }

    public void setPasswordMail(String passwordMail) {
        this.passwordMail = passwordMail;
    }

    public int getCodeBoite() {
        return codeBoite;
    }

    public void setCodeBoite(int codeBoite) {
        this.codeBoite = codeBoite;
    }
}
